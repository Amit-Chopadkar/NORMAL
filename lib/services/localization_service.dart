  // Initialize localization
  static Future<void> initialize() async {
    if (!Hive.isBoxOpen(boxName)) {
      await Hive.openBox(boxName);
    }
    _languageNotifier.value = getCurrentLanguage();
  }

  // Get current language
  static String getCurrentLanguage() {
    try {
      final box = Hive.box(boxName);
      return box.get(languageKey, defaultValue: 'en');
    } catch (e) {
      return 'en';
    }
  }

  // Set language
  static Future<void> setLanguage(String languageCode) async {
    try {
      final box = Hive.box(boxName);
      await box.put(languageKey, languageCode);
      _languageNotifier.value = languageCode;
    } catch (e) {
      print('Error setting language: $e');
    }
  }

  // Get translated string
  static String translate(String key) {
    final currentLanguage = _languageNotifier.value;
    return translations[currentLanguage]?[key] ?? translations['en']?[key] ?? key;
  }

  // Get all available languages
  static List<String> getAvailableLanguages() {
    return translations.keys.toList();
  }

  static ValueListenable<String> get languageNotifier => _languageNotifier;

